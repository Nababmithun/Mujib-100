package com.ezzetech.mujib100.model;

public class Recognize {
    private int image;
    private String title;
    private String name;
    private String description;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Recognize(int image, String title, String name, String description) {
        this.image = image;
        this.title = title;
        this.name = name;
        this.description = description;
    }

    public Recognize(int image, String title, String description) {
        this.image = image;
        this.title = title;
        this.description = description;
    }

    public int getImage() {
        return image;
    }

    public void setImage(int image) {
        this.image = image;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }
}
